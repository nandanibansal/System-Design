public class Location {
    int buildingNo;
    String area;
    String city;
    String state;
    String country;
    int pinCode;

    Location(int buildingNo, String area, String city, String state, String country, int pinCode) {
        this.buildingNo = buildingNo;
        this.area = area;
        this.city = city;
        this.state = state;
        this.country = country;
        this.pinCode = pinCode;
    }
}
